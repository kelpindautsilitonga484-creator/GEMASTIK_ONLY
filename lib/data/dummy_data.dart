  static void addBooking(BookingModel booking) {
    userBookings.insert(0, booking);
    booking.travel.occupiedSeats.addAll(booking.selectedSeats);
  }

  static void cancelBooking(String bookingId) {
    final index = userBookings.indexWhere((b) => b.bookingId == bookingId);
    if (index != -1) {
      final booking = userBookings[index];
      // remove seats from travel
      booking.travel.occupiedSeats.removeWhere((seat) => booking.selectedSeats.contains(seat));
      // update status
      userBookings[index] = BookingModel(
        bookingId: booking.bookingId,
        travel: booking.travel,
        passengerName: booking.passengerName,
        passengerPhone: booking.passengerPhone,
        selectedSeats: booking.selectedSeats,
        totalPrice: booking.totalPrice,
        paymentMethod: booking.paymentMethod,
        status: 'Dibatalkan',
        bookingDate: booking.bookingDate,
      );
    }
  }
